package chap01_print;

public class Print_String_03 {

	public static void main(String[] args) {
		System.out.print("3+5=");
		System.out.println("8");
		
		//연산은 쌍따음표("")를 넣지 않는다
		System.out.println(3+5);
		System.out.println(5-3);
		System.out.println(5*3);
		System.out.println(5/3);
		//정수/정수=정수 : 몫만 출력 ->소수점 자리를 표시해주지 않는다
		
	}
	
}
